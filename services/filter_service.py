import re

# 高考 / 考研 / 专业 / 就业相关关键词
KEYWORDS = [
    # 高考
    "高考", "志愿", "填报", "分数线", "录取", "大学", "院校",
    "本科", "专科", "一本到", "二本", "985", "211", "双一流",

    # 专业
    "专业", "学科", "课程", "计算机", "软件", "人工智能",
    "医学", "法律", "金融", "经济", "管理", "教育",
    "机械", "电子", "电气", "自动化", "建筑", "设计",

    # 考研
    "考研", "研究生", "硕士", "博士", "保研", "推免",
    "复试", "初试", "导师", "论文",

    # 就业
    "就业", "工作", "职业", "行业", "公司",
    "招聘", "面试", "薪资", "工资", "待遇", "前景",

    # 学习
    "学习", "备考", "复习", "考试", "成绩",

    # 规划
    "规划", "方向", "未来", "发展", "建议"
]


# 常见问题模式
PATTERNS = [
    r".*什么专业.*好.*",
    r".*哪个大学.*好.*",
    r".*就业.*怎么样.*",
    r".*薪资.*多少.*",
    r".*前景.*如何.*",
    r".*适合.*学.*什么.*",
    r".*推荐.*专业.*",
    r".*比较.*专业.*",
    r".*考研.*难.*吗.*",
]


def is_related_question(question: str) -> bool:
    """
    判断用户问题是否与：
    高考 / 考研 / 专业 / 就业 / 学习 相关
    """

    if not question:
        return False

    q = question.lower()

    # 关键词检测
    for word in KEYWORDS:
        if word in q:
            return True

    # 正则检测
    for pattern in PATTERNS:
        if re.match(pattern, q):
            return True

    return False